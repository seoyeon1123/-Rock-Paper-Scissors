-- =========================================================================
-- players 테이블 PK를 (id) → (room_id, id) 복합키로 변경
-- =========================================================================
-- 이유: 같은 플레이어 ID가 여러 방에 동시에 존재할 수 있어야 함.
-- (방 A 생성 후 방 B를 만들거나 참여할 때 PK 충돌 방지)
-- =========================================================================

alter table players drop constraint players_pkey;
alter table players add primary key (room_id, id);
